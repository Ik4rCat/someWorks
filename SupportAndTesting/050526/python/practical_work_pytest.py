import pytest


# ==============================
# РЕАЛИЗАЦИЯ ФУНКЦИИ
# ==============================

def get_even_numbers(numbers):
    """Возвращает список чётных чисел из входного списка."""
    if not isinstance(numbers, list):
        raise TypeError("Ожидается список")
    return [n for n in numbers if n % 2 == 0]


# ==============================
# ЧЕК-ЛИСТ
# ==============================
# 1. Список с чётными и нечётными числами — возвращаются только чётные    +
# 2. Список только из нечётных — возвращается пустой список                +
# 3. Список только из чётных — возвращается тот же список                  +
# 4. Пустой список — возвращается пустой список                            +
# 5. Список с нулём — ноль считается чётным                               +
# 6. Отрицательные чётные числа — включаются в результат                  +
# 7. Некорректный тип входных данных → TypeError                           +


# ==============================
# ТЕСТ-КЕЙСЫ (таблица)
# ==============================
# №  Входные данные              Ожидаемый результат        Описание
# 1  [1, 2, 3, 4]                [2, 4]                     смешанный список
# 2  [1, 3, 5, 7]                []                         только нечётные
# 3  [2, 4, 6]                   [2, 4, 6]                  только чётные
# 4  []                          []                         пустой список
# 5  [0, 1, 2]                   [0, 2]                     ноль — чётный
# 6  [-4, -3, -2, -1]            [-4, -2]                   отрицательные чётные
# 7  "строка"                    TypeError                  неверный тип


# ==============================
# UNIT-ТЕСТЫ
# ==============================

@pytest.mark.parametrize("input_list, expected", [
    ([1, 2, 3, 4], [2, 4]),
    ([1, 3, 5, 7], []),
    ([2, 4, 6], [2, 4, 6]),
    ([], []),
    ([0, 1, 2], [0, 2]),
    ([-4, -3, -2, -1], [-4, -2]),
])
def test_get_even_numbers_parametrized(input_list, expected):
    assert get_even_numbers(input_list) == expected


def test_empty_list():
    assert get_even_numbers([]) == []


def test_zero_is_even():
    assert 0 in get_even_numbers([0])


def test_all_odd():
    assert get_even_numbers([1, 3, 5]) == []


def test_invalid_type_raises():
    with pytest.raises(TypeError):
        get_even_numbers("строка")


def test_single_even():
    assert get_even_numbers([42]) == [42]


def test_negative_evens():
    result = get_even_numbers([-4, -2, -1, 3])
    assert result == [-4, -2]
