# Функция: second_largest(lst) — второй по величине элемент

# ============================================================
# Чек-лист
# ============================================================
# [x] Пустой список → исключение или None
# [x] Список из одного элемента → исключение или None
# [x] Все элементы одинаковые → None (нет второго уникального)
# [x] Отрицательные значения → корректный результат
# [x] Наличие дубликатов → второй УНИКАЛЬНЫЙ максимум
# [x] Обычный список → верный результат
# [x] Список из двух разных элементов → минимум из них

# ============================================================
# Реализация
# ============================================================
def second_largest(lst):
    if not lst or len(lst) < 2:
        return None
    unique = list(set(lst))
    if len(unique) < 2:
        return None
    unique.sort(reverse=True)
    return unique[1]


# ============================================================
# Test cases (таблица)
# ============================================================
# | # | Входные данные         | Ожидаемый результат | Описание              |
# |---|------------------------|---------------------|-----------------------|
# | 1 | []                     | None                | Пустой список         |
# | 2 | [5]                    | None                | Один элемент          |
# | 3 | [3, 3, 3]              | None                | Все одинаковые        |
# | 4 | [-5, -1, -3]           | -3                  | Отрицательные         |
# | 5 | [1, 2, 2, 3]           | 2                   | Дубликаты             |
# | 6 | [10, 20, 30]           | 20                  | Обычный список        |
# | 7 | [7, 3]                 | 3                   | Два разных элемента   |
# | 8 | [0, 0, 1]              | 0                   | Нули + уникальный     |
# | 9 | [100, 1, 50, 100, 50]  | 50                  | Много дубликатов      |
# |10 | [-10, -10, -5]         | -10                 | Отриц. + дубликат     |

import pytest

def test_empty_list():
    assert second_largest([]) is None

def test_single_element():
    assert second_largest([5]) is None

def test_all_same():
    assert second_largest([3, 3, 3]) is None

def test_negative_values():
    assert second_largest([-5, -1, -3]) == -3

def test_duplicates():
    assert second_largest([1, 2, 2, 3]) == 2

def test_normal():
    assert second_largest([10, 20, 30]) == 20

def test_two_elements():
    assert second_largest([7, 3]) == 3

def test_zeros_and_unique():
    assert second_largest([0, 0, 1]) == 0

def test_many_duplicates():
    assert second_largest([100, 1, 50, 100, 50]) == 50

def test_negative_with_duplicate():
    assert second_largest([-10, -10, -5]) == -10
